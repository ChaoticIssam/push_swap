#include "checker.h"

int	arg_sorted(char **av)
{
	int	i;
	int	j;

	i = 0;
	j = i + 1;
	while (*av)
	{
		if (av[i] < av[j])
			return (1);
		i++;
		j++;
	}
	return (0);
}
